// Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração, multipliacaçaõ e divisão desses valores.

function calculadora(n1, n2) {
    console.log(`Números informados ${n1}, ${n2}
Soma = ${n1 + n2}
Subtração = ${n1 - n2}
Multiplicação = ${n1 * n2}
Divisão = ${n1 / n2}`)
}

calculadora(1, 2)
